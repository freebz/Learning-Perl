# 문자열

# 작은따옴표로 묶은 문자열 상수

'fred'      # 네 개의 문자 : f, r, e, d
'barney'    # 6개의 문자
''          # 널 문자(문자가 없음)
'Don\'t let an apostrophe end this strig prematurely!'
            # 역슬래시 없이 생략부호(')를 사용하면 문자열이 일찍 끝나버립니다.
'the last character of this string is a backslash: \\'
            # 문자열의 마지막 문자는 역슬래시입니다.
'hello\n'   # hello 다음 역슬래시와 n
'hello
there'      # hello, 줄바굼 문자, there(총 11개의 문자)
'\'\\'      # 작은따옴표와 역슬래시
